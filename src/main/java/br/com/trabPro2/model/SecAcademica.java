package br.com.trabPro2.model;

public class SecAcademica {
      public char Funcionario;
      public char Diretor;
      public char Presidente;

    public char getFuncionario() {
        return Funcionario;
    }

    public void setFuncionario(char funcionario) {
        Funcionario = funcionario;
    }

    public char getDiretor() {
        return Diretor;
    }

    public void setDiretor(char diretor) {
        Diretor = diretor;
    }

    public char getPresidente() {
        return Presidente;
    }

    public void setPresidente(char presidente) {
        Presidente = presidente;
    }
}
